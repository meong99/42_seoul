#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*(char *)s)
	{
		if (*(char *)s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (0);
}
